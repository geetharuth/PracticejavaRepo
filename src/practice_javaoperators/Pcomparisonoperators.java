package practice_javaoperators;

public class Pcomparisonoperators {

	public static void main(String[] args) {
		// comparision operators
		int x=5;
		int y=2;
		System.out.println(x==y);
		System.out.println(x!=y);
		System.out.println(x>y);
		System.out.println(x<y);
		System.out.println(x>=y);
		System.out.println(x<=y);
		
		
		
		

	}

}
