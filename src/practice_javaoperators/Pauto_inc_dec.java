package practice_javaoperators;

public class Pauto_inc_dec {

	public static void main(String[] args) {
		// Auto increment and decrement
		int A=4;
		A++;
		System.out.println("increment for one time::"+A);
		int B=4;
		B--;
		System.out.println("decrement for one time::"+B);
		int C=50;
		C+=2;
		System.out.println("increment for more than one time::"+C);
		int D=50;
		D -=2;
		System.out.println("decrement for more than one time::"+D);

	}

}
