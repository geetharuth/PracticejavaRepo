package practice_javaoperators;

public class Parthematicoperators {

	public static void main(String[] args) {
		// arthematic operator
		System.out.println(3+6);
		System.out.println("-------------------------");
		int x=2+6;
		System.out.println(x);
		System.out.println("-------------------------");
		int sum1=2;
		int sum2=sum1+5;
		int sum3=sum2+6;
		System.out.println(sum3);
		System.out.println("-------------------------");
		int k=2;
		int L=5;
		System.out.println(k+L);
		System.out.println(k-L);
		System.out.println(k*L);
		System.out.println(k/L);
		System.out.println(k%L);
		
	}

}
