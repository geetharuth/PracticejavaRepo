package practicejavaLoopings;

public class Pprint2table {

	public static void main(String[] args) {
		// print 2 table
		int number = 2;
		for(int i=1;i<=10;i++)
		{
			System.out.println(number+"x"+i+"="+(number *i));
		}
		System.out.println(".............................................................");
			//print 10 table
		int num1 = 10;
		for(int K=0;K<=10;K++)
		{
			System.out.println(num1+"x"+K+"="+(num1 *K));
		}

	}

}
