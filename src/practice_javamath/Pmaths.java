package practice_javamath;

public class Pmaths {

	public static void main(String[] args) {
		// highest value of x and y:
		System.out.println(Math.min(2, 1));
		//Lowest value of X and Y:
		System.out.println(Math.max(232, 2441));
		//square root of x:
		System.out.println(Math.sqrt(64));
		//absolute (positive) value of x:
		System.out.println(Math.abs(-4.7));
		//random number between 0.0 (inclusive), and 1.0 (exclusive):
		System.out.println(1.0);
	}

}
