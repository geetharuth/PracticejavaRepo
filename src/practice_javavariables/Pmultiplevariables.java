package practice_javavariables;

public class Pmultiplevariables {

	public static void main(String[] args) {
		// one variable of the same type,
int x=10;
int y=20;
int z=30;
System.out.println(x+y+z);
//You can simply write:
int x1=5,y1=10,z1=30;
System.out.println(x+y+z);
// same value to multiple variables in one line:
int x11=y=z=50;
System.out.println(x11+y+z);



	}

}
