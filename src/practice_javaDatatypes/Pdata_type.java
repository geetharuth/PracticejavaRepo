package practice_javaDatatypes;

public class Pdata_type {

	public static void main(String[] args) {
		 int myNum = 5;   
		 System.out.println(myNum);// Integer (whole number)
		float myFloatNum = 5.99f;
		System.out.println(myFloatNum);// Floating point number
		char myLetter = 'D';  
		System.out.println(myLetter);// Character
		boolean myBool = true;   
		System.out.println(myBool );// Boolean
		String myText = "Hello";  
		System.out.println(myText);// String
	}

}
